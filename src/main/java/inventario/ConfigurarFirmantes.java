        codigoSeccion=consulta_seccion.codigo_empresa();
        ConexionConsultarDatosFirmantes datos=new ConexionConsultarDatosFirmantes();
        datos.setCodigo_seccion(codigoSeccion);
        datos.consulta();
        cedulaFirmante=datos.getCedula_firmantes();
