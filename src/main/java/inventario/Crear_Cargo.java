    private void Campo_descripcionKeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_Campo_descripcionKeyPressed
        // esta clase la uso
        //pra comprobar que se esta intentando guardar un cargo que ya existe
        char caracter = evt.getKeyChar();
        
        String temporal;
        for (int i=0; i<descripciones.size(); i++)
        {
             temporal=descripciones.get(i);
             System.out.println(temporal);
             
                 if( ((temporal.charAt(i)))==(caracter) )
                {
                    Etiq_previa.setText("Cargo similar ya guardado en base de datos: "+descripciones.get(i));
                }//if
        }//for
        
    }//GEN-LAST:event_Campo_descripcionKeyPressed

    private void Boton_guardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_Boton_guardarActionPerformed
        // TODO add your handling code here:
        if(Campo_descripcion.getText().trim().toUpperCase().isEmpty())
        {
          JOptionPane.showMessageDialog(null, "Debe completar todos los campos", "Advertencia", JOptionPane.INFORMATION_MESSAGE);
        }
        else
        {
            ConexionCrearCargos crear = new ConexionCrearCargos();
            crear.setDescripcion(Campo_descripcion.getText().trim().toUpperCase());
            crear.crear();
            if(crear.respuesta()==1)
            {
                JOptionPane.showMessageDialog(null, "Informacion Ingresada Correctamente", "Informacion", JOptionPane.INFORMATION_MESSAGE);
                  Campo_descripcion.setText("");
            }
            else
            {
                JOptionPane.showMessageDialog(null, "No se puedo guardar la informacion", "Error Grave", JOptionPane.ERROR_MESSAGE);
            }
        }
        
    }//GEN-LAST:event_Boton_guardarActionPerformed

